x = int(input("Enter any number (0 to 5): "))
if x == 0:
    print(x, ":Zero")
elif x == 1:
    print(x, ":One")
elif x == 2:
    print(x, ":Two")
elif x == 3:
    print(x, ":Three")
elif x == 4:
    print(x, ":Four")
elif x == 5:
    print(x, ":Five")
else:
    print("Please enter number between 0 to 5")
